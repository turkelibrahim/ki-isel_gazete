/** Sources grouped by region with counts. */
export const REGION_SOURCE_COUNTS = REGIONAL_SOURCE_CATALOG.reduce((acc, src) => {
  acc[src.region] = (acc[src.region] || 0) + 1;
  return acc;
}, {});

export function getSourcesByRegion(region) {
  if (!region || region === "global") return REGIONAL_SOURCE_CATALOG.filter((s) => s.enabled);
  return REGIONAL_SOURCE_CATALOG.filter((s) => s.enabled && (s.region === region || s.isGlobalSource));
}

export function getEnabledSources() {
  return REGIONAL_SOURCE_CATALOG.filter((s) => s.enabled);
}

export function getActiveRssSources() {
  return REGIONAL_SOURCE_CATALOG.filter((s) => s.enabled && s.rssUrl);
}

export function getSourceById(id) {
  return REGIONAL_SOURCE_CATALOG.find((s) => s.id === id) || null;
}

export function getSourceMetadata(sourceName) {
  const normalized = String(sourceName || "").toLowerCase().trim();
  return REGIONAL_SOURCE_CATALOG.find((s) =>
    s.sourceName.toLowerCase() === normalized ||
    s.id === normalized
  ) || null;
}

export function regionLabel(region) {
  return REGION_LABELS[region] || "Global";
}

export function summarizeCatalog() {
  const byRegion = {};
  for (const region of CANONICAL_REGIONS) {
    byRegion[region] = REGIONAL_SOURCE_CATALOG.filter((s) => s.region === region && s.enabled);
  }
  return {
    total: REGIONAL_SOURCE_CATALOG.length,
    enabled: REGIONAL_SOURCE_CATALOG.filter((s) => s.enabled).length,
    globalSources: REGIONAL_SOURCE_CATALOG.filter((s) => s.isGlobalSource).length,
    byRegion: Object.fromEntries(
      Object.entries(byRegion).map(([r, sources]) => [r, sources.length])
    )
  };
}
